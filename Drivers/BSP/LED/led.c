 #include "./BSP/LED/led.h"

/**
 * @brief       初始化LED相关IO口, 并使能时钟
 * @param       无
 * @retval      无
 */
void led_init(void)
{
    GPIO_InitTypeDef gpio_init_struct;
    
    LED0_GPIO_CLK_ENABLE();                                 /* LED0时钟使能 */
    LED1_GPIO_CLK_ENABLE();                                 /* LED1时钟使能 */
    engine_relay_GPIO_CLK_ENABLE();                                 
    motor_relay_GPIO_CLK_ENABLE();                                 
    rectifier_relay_GPIO_CLK_ENABLE();                                 

    gpio_init_struct.Pin = LED0_GPIO_PIN;                   /* LED0引脚 */
    gpio_init_struct.Mode = GPIO_MODE_OUTPUT_PP;            /* 推挽输出 */
    gpio_init_struct.Pull = GPIO_PULLUP;                    /* 上拉 */  
    gpio_init_struct.Speed = GPIO_SPEED_FREQ_HIGH;          /* 高速 */
    HAL_GPIO_Init(LED0_GPIO_PORT, &gpio_init_struct);       /* 初始化LED0引脚 */

    gpio_init_struct.Pin = LED1_GPIO_PIN;                   /* LED1引脚 */
    HAL_GPIO_Init(LED1_GPIO_PORT, &gpio_init_struct);       /* 初始化LED1引脚 */
    
		gpio_init_struct.Pull = GPIO_NOPULL;                    /* 不上拉不拉低 */
    gpio_init_struct.Pin = engine_relay_GPIO_PIN;                   /* 电源继电器引脚 */
    HAL_GPIO_Init(engine_relay_GPIO_PORT, &gpio_init_struct);       /* 初始化电源继电器引脚 */

    gpio_init_struct.Pin = motor_relay_GPIO_PIN;                   /* 电调继电器引脚 */
    HAL_GPIO_Init(motor_relay_GPIO_PORT, &gpio_init_struct);       /* 初始化电调继电器引脚 */

    gpio_init_struct.Pin = rectifier_relay_GPIO_PIN;                   /* 整流器引脚 */
    HAL_GPIO_Init(rectifier_relay_GPIO_PORT, &gpio_init_struct);       /* 初始化整流器引脚 */

	LED0(1);                                                /* 关闭 LED0 */
    LED1(1);                                                /* 关闭 LED1 */
		//关闭所有继电器
	Engine_relay(1);
	Motor_relay(1);
	Rectifier_relay(1);
}

